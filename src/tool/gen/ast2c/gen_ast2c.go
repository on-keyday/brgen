package main

import (
	"fmt"
	"io"
	"log"
	"os"
	"path/filepath"
	"strings"

	"github.com/iancoleman/strcase"
	"github.com/on-keyday/brgen/src/tool/gen"
)

const prefix = "ast2c_"

func generateHeader(rw io.Writer, defs *gen.Defs, single bool) {
	w := gen.NewWriter(rw)
	w.Printf("// Code generated by gen_ast2c.go. DO NOT EDIT.\n\n")
	if !single {
		w.Printf("#pragma once\n#ifndef __AST_H__\n#define __AST_H__\n\n")
	} else {
		w.Printf("#include<string.h>\n")

	}
	w.Printf("#include <stdint.h>\n")
	w.Printf("#ifdef __cplusplus\n")

	w.Printf("extern \"C\" {\n")
	if single {
		w.Printf("#else\n")
		w.Printf("#include<stdalign.h>\n")
	}
	w.Printf("#endif\n\n")
	w.Printf(`
typedef struct %[1]sjson_handlers %[1]sjson_handlers;
struct %[1]sjson_handlers {
	void* ctx;
	void* (*object_get)(%[1]sjson_handlers*, void* obj, const char* name);
	void* (*array_get)(%[1]sjson_handlers*, void* obj, size_t i);
	// return non-zero for success, 0 for error
	int (*array_size)(%[1]sjson_handlers*, void* obj, size_t* size);
	int (*is_null)(%[1]sjson_handlers*, void* obj);
	int (*is_array)(%[1]sjson_handlers*, void* obj);
	int (*is_object)(%[1]sjson_handlers*, void* obj);
	char* (*string_get_alloc)(%[1]sjson_handlers*, void* obj);
	const char* (*string_get)(%[1]sjson_handlers*, void* obj);
	// returns non-zero for success, 0 for error
	int (*number_get)(%[1]sjson_handlers*, void* obj, uint64_t* n);
	// returns 0 or 1. -1 for error
	int (*boolean_get)(%[1]sjson_handlers*, void* obj);

	void* (*alloc)(%[1]sjson_handlers*, size_t size, size_t align);
	void (*free)(%[1]sjson_handlers*, void* ptr);

	void (*error)(%[1]sjson_handlers*, void* ptr, const char* msg);
};

`, prefix)
	// declare all struct types
	for _, def := range defs.Defs {
		switch d := def.(type) {
		case *gen.Interface:
			w.Printf("typedef struct %s %s;\n", d.Name, d.Name)
		case *gen.Struct:
			w.Printf("typedef struct %s %s;\n", d.Name, d.Name)
		case *gen.Enum:
			w.Printf("typedef enum %s %s;\n", d.Name, d.Name)
		}
	}
	// next non pointer or interface types
	for _, def := range defs.Defs {
		switch d := def.(type) {
		case *gen.Interface:
			continue
		case *gen.Struct:
			if len(d.Implements) == 0 && d.Name != prefix+"Scope" {
				w.Printf("struct %s {\n", d.Name)
				for _, field := range d.Fields {
					w.Printf("\t%s %s;\n", field.Type.CString(), field.Name)
					if field.Type.IsArray {
						w.Printf("\tsize_t %s_size;\n", field.Name)
					}
				}
				w.Printf("};\n\n")
				if d.Name == prefix+"Loc" || d.Name == prefix+"Pos" {
					w.Printf("int %s_parse(%s*,%sjson_handlers*,void*);\n\n", d.Name, d.Name, prefix)
				}
			}
		case *gen.Enum:
			w.Printf("enum %s {\n", d.Name)
			for _, field := range d.Values {
				field.Name = strings.ToUpper(d.Name) + "_" + strings.ToUpper(field.Name)
				w.Printf("\t%s,\n", field.Name)
			}
			w.Printf("};\n")
			w.Printf("const char* %s_to_string(%s);\n", d.Name, d.Name)
			w.Printf("int %s_from_string(const char*,%s*);\n\n", d.Name, d.Name)
		}
	}
	// next pointer types
	for _, def := range defs.Defs {
		switch d := def.(type) {
		case *gen.Interface:
			w.Printf("struct %s {\n", d.Name)
			w.Printf("\tconst ast2c_NodeType node_type;\n")
			for _, field := range d.Fields {
				w.Printf("\t%s %s;\n", field.Type.CString(), field.Name)
			}
			w.Printf("};\n\n")
		case *gen.Struct:
			if len(d.Implements) == 0 && d.Name != "Scope" {
				continue
			}
			w.Printf("struct %s {\n", d.Name)
			w.Printf("\tconst %sNodeType node_type;\n", prefix)
			for _, field := range d.Fields {
				w.Printf("\t%s %s;\n", field.Type.CString(), field.Name)
				if field.Type.IsArray {
					w.Printf("\tsize_t %s_size;\n", field.Name)
				}
			}
			w.Printf("};\n\n")
			w.Printf("// returns 1 if succeed 0 if failed\n")
			w.Printf("int %s_parse(%sNode**,%sScope**,%s*,%sjson_handlers*,void*);\n\n", d.Name, prefix, prefix, d.Name, prefix)
		}
	}

	if !single {
		w.Printf("#ifdef __cplusplus\n")
		w.Printf("}\n")
		w.Printf("#endif\n\n")
		w.Printf("#endif\n")
	}
}

func generateSource(rw io.Writer, defs *gen.Defs, single bool) {
	w := gen.NewWriter(rw)
	if !single {
		w.Printf("// Code generated by gen_ast2c.go. DO NOT EDIT.\n\n")
		w.Printf("#include \"ast.h\"\n")
		w.Printf("#include<string.h>\n\n")
		w.Printf("#ifdef __cplusplus\n")
		w.Printf("extern \"C\" {\n")
		w.Printf("#else\n")
		w.Printf("#include<stdalign.h>\n")
		w.Printf("#endif\n\n")

	}

	for _, def := range defs.Defs {
		switch v := def.(type) {
		case *gen.Enum:
			w.Printf("const char* %s_to_string(%s val) {\n", v.Name, v.Name)
			w.Printf("\tswitch(val) {\n")
			for _, field := range v.Values {
				w.Printf("\tcase %s: return %q;\n", field.Name, field.Value)
			}
			w.Printf("\tdefault: return NULL;\n")
			w.Printf("\t}\n")
			w.Printf("}\n\n")
			w.Printf("// returns 1 if succeed 0 if failed\n")
			w.Printf("int %s_from_string(const char* str, %s* out) {\n", v.Name, v.Name)
			w.Printf("\tif (!str||!out) return 0;\n")
			for _, field := range v.Values {
				w.Printf("\tif (strcmp(str, %q) == 0) {\n", field.Value)
				w.Printf("\t\t*out = %s;\n", field.Name)
				w.Printf("\t\treturn 1;\n")
				w.Printf("\t}\n")
			}
			w.Printf("\treturn 0;\n")
			w.Printf("}\n\n")
		}
	}

	getAlloc := func(typ string) string {
		return "(" + typ + "*)h->alloc(h, sizeof(" + typ + "), alignof(" + typ + "))"
	}

	getObj := func(target, name string) string {
		return fmt.Sprintf("h->object_get(h, %s, %q)", target, name)
	}

	checkObj := func(target string) string {
		return fmt.Sprintf("if (!%s) { goto error; }", target)
	}

	reportError := func(target, err string) string {
		return fmt.Sprintf("if(h->error) { h->error(h,%s, %q); }", target, err)
	}

	checkObjNull := func(target string) string {
		return fmt.Sprintf("if (!%s) { %s return 0; }", target, reportError(target, fmt.Sprintf("%s is null", target)))
	}

	initObj := func(to, from, name string) string {
		return fmt.Sprintf("void* %s = %s;", to, getObj(from, name))
	}

	_ = checkObj
	_ = initObj
	_ = getAlloc

	for _, def := range defs.Defs {
		switch v := def.(type) {
		case *gen.Struct:
			if len(v.Implements) == 0 && v.Name != "Scope" {
				continue
			}
			w.Printf("// returns 1 if succeed 0 if failed\n")
			w.Printf("int %s_parse(%sNode** nodes,%sScope** scopes,%s* s,%sjson_handlers* h, void* obj) {\n", v.Name, prefix, prefix, v.Name, prefix)
			w.Printf("\tif (!nodes||!scopes||!s||!h||!obj) {\n")
			w.Printf("\t\t%s\n", reportError("NULL", "invalid argument"))
			w.Printf("\t\treturn 0;\n")
			w.Printf("\t}\n")

			for _, field := range v.Fields {
				if field.Type.Name == "char*" || field.Type.IsArray || field.Type.IsInterface || field.Type.IsPtr || field.Type.IsOptional {
					w.Printf("\ts->%s = NULL;\n", field.Name)
				}
			}

			w.Printf("\t%s\n", initObj("obj_body", "obj", "body"))
			w.Printf("\t%s\n", checkObjNull("obj_body"))
			for _, field := range v.Fields {
				if field.Name == "loc" {
					continue
				}
				w.Printf("\t%s\n", initObj(field.Name, "obj_body", field.Name))
			}
			w.Printf("\t%s\n", initObj("loc", "obj", "loc"))
			for _, field := range v.Fields {
				w.Printf("\t%s\n", checkObjNull(field.Name))
				if field.Type.IsArray {
					w.Printf("\tif(!h->array_size(h, %s,&s->%s_size)) {\n", field.Name, field.Name)
					w.Printf("\t\t%s\n", reportError(field.Name, fmt.Sprintf("failed to get array size of %s::%s", v.Name, field.Name)))
					w.Printf("\t\treturn NULL;\n")
					w.Printf("\t}\n")
				}
			}

			for _, field := range v.Fields {
				if field.Type.Name == "Loc" {
				}
			}

			w.Printf("}\n\n")
		}
	}

	w.Printf("#ifdef __cplusplus\n")
	w.Printf("}\n")
	w.Printf("#endif\n\n")

}

func main() {
	if len(os.Args) != 2 && len(os.Args) != 3 {
		log.Println("usage: gen_ast2c [<header file> <source file>] | /dev/stdout")
		return
	}

	hdr := os.Args[1]
	var src string
	if len(os.Args) == 3 {
		src = os.Args[2]
	} else {
		src = hdr
	}

	log.SetFlags(log.Flags() | log.Lshortfile)

	list, err := gen.LoadFromDefaultSrc2JSON()

	if err != nil {
		log.Println(err)
		return
	}

	defs, err := gen.CollectDefinition(list, func(s string) string {
		return strcase.ToSnake(s)
	}, func(s string) string {
		return prefix + strcase.ToCamel(s)
	}, map[string]string{
		"uint":    "uint64_t",
		"uintptr": "uint64_t",
		"bool":    "int",
		"string":  "char*",
		"any":     "void*",
	})

	if err != nil {
		log.Println(err)
		return
	}

	if hdr == "/dev/stdout" {
		generateHeader(os.Stdout, defs, false)
		generateSource(os.Stdout, defs, false)
		return
	}

	hdr, err = filepath.Abs(hdr)

	if err != nil {
		log.Println(err)
		return
	}

	src, err = filepath.Abs(src)
	if err != nil {
		log.Println(err)
		return
	}

	f, err := os.Create(hdr)

	if err != nil {
		log.Println(err)
		return
	}
	defer f.Close()

	if hdr == src {
		generateHeader(f, defs, true)
		generateSource(f, defs, true)
		return
	}

	f2, err := os.Create(src)
	if err != nil {
		log.Println(err)
		return
	}
	defer f2.Close()

	generateHeader(f, defs, false)
	generateSource(f2, defs, false)

}
