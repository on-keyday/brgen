import json

def generate_ebm_tablegen(schema):
    structs = {s["name"]: s for s in schema.get("structs", [])}
    enums = {e["name"]: e for e in schema.get("enums", [])}
    # subset_infoの階層構造を整理
    subsets = {
        s["name"]: {item["kind"]: item["fields"] for item in s["available_field_per_kind"]}
        for s in schema.get("subset_info", [])
    }

    output = []
    output.append("// Autogenerated EBM Dialect ODS\n")
    output.append("def EBM_Dialect : Dialect {")
    output.append("  let name = \"ebm\";")
    output.append("  let cppNamespace = \"::mlir::ebm\";")
    output.append("}\n")
    output.append("class EBM_Op<string mnemonic, list<Trait> traits = []> :")
    output.append("    Op<EBM_Dialect, mnemonic, traits>;\n")

    # 1. Enum定義
    for enm in schema.get("enums", []):
        output.append(f"def EBM_{enm['name']}Attr : I32EnumAttr<\"{enm['name']}\", \"EBM {enm['name']}\", [")
        for m in enm["members"]:
            output.append(f"  I32EnumAttrCase<\"{m['name']}\", {m['value']}>,")
        output.append(f"]> {{ let cppNamespace = \"::mlir::ebm\"; }}\n")

    def map_mlir_type(field_type_name, is_subset_context):
        # subsetの直下、または展開された内部フィールドは「必須」として扱う
        is_ptr = False if is_subset_context else False # 個別判断が必要な場合はここを拡張
        
        t = field_type_name
        if t == "ExpressionRef": base_t = "AnyType"
        elif t == "TypeRef":     base_t = "EBM_Type"
        elif t in ["IdentifierRef", "StringRef"]: base_t = "StringAttr"
        elif t == "bool":        base_t = "UnitAttr"
        elif t in enums:         base_t = f"EBM_{t}Attr"
        elif t in ["Varint", "std::uint64_t", "std::uint8_t"]: base_t = "I64Attr"
        else:                    base_t = "AnyAttr"
        
        return base_t

    # --- Operation生成 ---
    for struct_name, kind_map in subsets.items():
        parent_struct = structs.get(struct_name)
        if not parent_struct: continue
        parent_fields = {f["name"]: f for f in parent_struct["fields"]}

        for kind, active_fields in kind_map.items():
            output.append(f"def EBM_{kind}Op : EBM_Op<\"{kind.lower()}\"> {{")
            
            final_args = []
            for f_name in active_fields:
                if f_name == "kind": continue
                f_def = parent_fields.get(f_name)
                if not f_def: continue

                f_type = f_def["type"]

                # 1段階の展開チェック: 構造体であり、かつRef系/基本型でない場合
                if f_type in structs and not f_type.endswith("Ref"):
                    inner_struct = structs[f_type]
                    for inner_f in inner_struct["fields"]:
                        # 名前衝突回避のため "親フィールド名_子フィールド名" にする
                        # 例: other_other2
                        flat_name = f"{f_name}_{inner_f['name']}"
                        mlir_t = map_mlir_type(inner_f["type"], is_subset_context=True)
                        final_args.append(f"    {mlir_t}:${flat_name}")
                else:
                    # 通常のフィールド
                    mlir_t = map_mlir_type(f_type, is_subset_context=True)
                    final_args.append(f"    {mlir_t}:${f_name}")

            output.append("  let arguments = (ins\n" + ",\n".join(final_args) + "\n  );")
            
            if "Expression" in struct_name:
                output.append("  let results = (outs AnyType:$res);")
            
            # Region判定 (Blockなどを含む場合)
            if any(parent_fields[f]["type"] in ["Block", "StatementRef"] for f in active_fields if f in parent_fields):
                output.append("  let regions = (region AnyRegion:$body);")
                
            output.append("}\n")

    return "\n".join(output)



if __name__ == "__main__":
    import subprocess as sp
    TOOL_PATH = "tool/ebmcodegen" + (".exe" if sp.os.name == "nt" else "")
    result = sp.run([TOOL_PATH, "--mode","spec-json"], capture_output=True, text=True)
    json_data = json.loads(result.stdout)
    td_output = generate_ebm_tablegen(json_data)
    print(td_output)